    public void populaModelo(){
        LinkedList<Jogos> lojinha = Banco.listaJogos();
        modelo = (DefaultTableModel) tabela.getModel();
        if(modelo.getColumnCount()<3){
            modelo.addColumn("Jogos");
            modelo.addColumn("PreÃ§o");
            modelo.addColumn("Quantidade");
        }

        
        while (modelo.getRowCount() > 0) {
            modelo.removeRow(0);
        }
        for (Jogos j : lojinha) {
            
            modelo.addRow(new Object[]{j.getJogos(),j.getPreco(),j.getCont()});            
        }   
    }

