    private void botaoOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoOkActionPerformed
       LinkedList<Jogos> lojinha = Banco.listaJogos();
        int cont = lojinha.size() + 1;
        
        for(Jogos j: lojinha){
                    while(j.getId() == cont){
                        cont++;
                    }
                }
        try{
        j = new Jogos();
        j.setJogos(fieldNome.getText());
        j.setPreco(Integer.parseInt(fieldValor.getText()));
        j.setId(cont);
        j.setQuantidade(Integer.parseInt(fieldQuant.getText()));
        if(verificaSeExiste(lojinha)){
            Banco.salvarJogos(j);
            
            
        fieldNome.setText("");
        fieldValor.setText(""); 
        fieldQuant.setText("");
        
        JOptionPane.showMessageDialog(null, "O jogo foi cadastrado");
        }
        else {
           Banco.atualizaBancoJogos(j);
        }
        }
        catch(Exception e){
          JOptionPane.showMessageDialog(null, "Por favor preencha adequadamente os dados.");  
        }
    }//GEN-LAST:event_botaoOkActionPerformed

