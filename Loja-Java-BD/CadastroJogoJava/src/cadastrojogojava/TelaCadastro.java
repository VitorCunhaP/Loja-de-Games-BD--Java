    public boolean verificaSeExiste(LinkedList<Jogos> lojinha){
        boolean r = true;
        for (Jogos j : lojinha) {
            if((j.getPreco() == Integer.parseInt(fieldValor.getText())) && j.getJogos().equals(fieldNome.getText() )) {
            r = false;
            j.incrementaQuantidade();
            JOptionPane.showMessageDialog(null, "Foi adicionado a quantidade");
            fieldNome.setText("");
            fieldValor.setText(""); 
            break;

             }    
             else if((j.getPreco() != Integer.parseInt(fieldValor.getText())) && j.getJogos().equals(fieldNome.getText() )){
               r = false;
               JOptionPane.showMessageDialog(null, "Esse jogo já está cadastrado com outro preço");
               break;
            }
        }        
        return r;
    }
    
