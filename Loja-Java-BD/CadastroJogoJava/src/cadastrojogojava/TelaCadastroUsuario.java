    private void ButtonCadastroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonCadastroActionPerformed
        LinkedList<Usuario> usuarios = Banco.listaUsuarios();
        int cont = usuarios.size() + 1;
        String mensagem = "";
        
        if(!fieldSenha.getText().equals(fieldSenhaR.getText())){
            mensagem = "As senhas não conferem";
        }else{
        
            if(!verfica(usuarios)){
            
                for(Usuario u: usuarios){
                    while(u.getId() == cont){
                        cont++;
                    }
                }
                        Usuario usuario = new Usuario();
                        usuario.setSenha(fieldSenha.getText());
                        usuario.setPerfil(2);
                        usuario.setUsername(fieldNome.getText());
                        usuario.setId(cont);
                        Banco.salvarUsuario(usuario);
                        mensagem = "Seu Usuário foi cadastrado.";   
                    
            }else{
                mensagem = "Username já está sendo utilizado."; 
            }
        
             
            }
        JOptionPane.showMessageDialog(null, mensagem);
        if(mensagem.equals("Seu Usuário foi cadastrado.")){
            dispose();
        }
        fieldNome.setText("");
        fieldSenha.setText("");
        fieldSenhaR.setText("");
        
    }//GEN-LAST:event_ButtonCadastroActionPerformed

