    public boolean verfica(LinkedList <Usuario> usuarios){
        boolean retorno = false;
        
        
        for(Usuario u: usuarios){
            if(u.getUsername().equals(fieldNome.getText())){
              retorno = true;  
              break;
            }
        }
        return retorno;
    }
